package com.example.smart_ticket_router.enums;

/**
 * Represents the priority level assigned to a support ticket.
 *
 * <p>The priority indicates how urgently a ticket should be
 * addressed by the assigned support team.
 */
public enum Priority {

    /**
     * High-priority ticket requiring immediate attention.
     */
    HIGH,

    /**
     * Medium-priority ticket that should be resolved
     * within the normal support timeframe.
     */
    MEDIUM,

    /**
     * Low-priority ticket with minimal business impact.
     */
    LOW
}